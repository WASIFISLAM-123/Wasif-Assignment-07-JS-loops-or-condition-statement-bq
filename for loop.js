
// FOR LOOP Q/A\ //

// 1) Print numbers from 1 to 10 using a for loop ?

for (let i = 1; i <= 10; i = i + 1) {
    console.log("Ans:1")
    console.log(i)
}

// 2) Print the even numbers between 1 and 20 using a for loop ?

for (let i = 2; i <= 20; i = i + 2) {
    console.log("Ans:2")
    console.log(i)
}

// 3) Print the multiplication table of 7 using a for loop ?

for (let i = 1; i <= 10; i = i + 1) {
    console.log("Ans:3")
    console.log("7 x " + i + " = " + (7 * i));
}

// 4) Print numbers from 10 down to 1 using a for loop ?

for (let i = 10; i >= 1; i = i - 1) {
    console.log("Ans:4")
    console.log(i)
}

// 5) Find the sum of first 50 natural numbers using a for loop ?

let sum = 0;
for (let i = 1; i <= 50; i = i + 1) {
    sum = sum + i;
}
console.log("Ans:5")
console.log("sum = " + sum);




